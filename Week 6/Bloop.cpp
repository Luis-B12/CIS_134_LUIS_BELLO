#include <iostream>

using namespace std;

int main()
{
    int num = 100;                             //100 is initialized to num
    
    for (num; num >= 1; num--)                 //when num is greater than num or equal to num, print num, and subtract one to num
    
    {
        cout << num << endl;
    }               
    

return 0;
}